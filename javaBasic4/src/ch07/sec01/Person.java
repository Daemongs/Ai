package ch07.sec01;

//부모클래스 = 슈퍼클래스 = 상위클래스
public class Person {
	
	String name;
	int age;
	
	public void eat() {
		System.out.println("먹는다");
	}
	
	
	public void asleep() {
		System.out.println("잔다");
	}
	
	
	public Person() {
		System.out.println("조상 할머니 부모 클래스입니다.");
	}
	
	public Person(String name, int age) {
		
		this.name = name;
		this.age = age;
	}
	
	

}
